#include <iostream>  //有用到cout, endl就要include
using namespace std; // 後續可直接調用cout, endl等

int main()
{
    cout << "第一題程式碼output:" << endl;
    // 效果同 cout << "第一題程式碼output\n";
    cout << "  ^    ^  " << endl;
    cout << "    O" << endl;
    cout << "第二題程式碼output:" << endl;
    cout << " /\\_/\\ " << endl;
    cout << "(O﹒O ) " << endl;
    cout << " > ^ <   " << endl;
    return 0;
}